# Ambos são programas. Um compilador analisa o programa por inteiro; um interpretador analisa linha por linha do programa. Em outras palavras,
# num compilador, o código-fonte não é compilado até que todos os erros sejam resolvidos e todos os erros são mostrados ao final da compilação;
# por outro lado, num interpretador, a execucação do código é feita linha após linha ao passo da análise e, no momento em que um erro surge,  
# sua análise para; como consequência, a execução é interrompida e uma msg de erro enviada. 
# Caso a análise seja bem-sucedida num compilador, um arquivo executável escrito em linguagem de máquina é produzido como resultado de sua
# análise e o mesmo pode ser armazenado na memória; enquanto um interpretador nada produz, por isso, é preciso uma nova análise do código-fonte
# toda vez que tiver interesse em executar o programa novamente.
# Desta forma, o tempo para análise do código num compilador é maior, embora a execução do programa seja mais rápida.

